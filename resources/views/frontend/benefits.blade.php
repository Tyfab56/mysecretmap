Vous pouvez utiliser My Secret map, soit en mode "abonné", soit en mode "partenaire photographe". Les 2 modes sont gratuits, mais le second vous perme de rejoindre notre équipe et d'être rémunéré pour vos participations.
Les "partenaires photographes" se partagenet 30% des revenus publicitaires du site, selon des modalités établies de façon équitable.
Pour devenir "photographe partenaire", vous devez remplir certaines conditions :
- Faire valider votre condidature en fournissant des exemples de votre travail de photographe 
- Accepter de proposer la création d'un minimum de 10 spots par an
- Remplir intégralement les informations concernant les spot proposés
- Etre en mesure d'émettre une facture avec une entité légale : société, auto-entreprise ou association.

Deux façons travailler ensemble :
Vous proposez la création de nouveau spots ou vous apportez un office du tourisme
Chaque proposition de spot validée et réalisée vous donne le droit à des crédits qui entrent dans le calcul du reversement publicitaire.
Les revenus publicitaires du site sont principalement de l'affiliation :
- Affiliation Hotelière
- Affiliation activités touristique
- Bandeaux publicitaires 

Ne rentre pas dans le calcul
- Commissions sur vente de médias
- Facturation Outils Offices du Tourisme


Etre un photographe partenaire à de nombreux avantages :
- Vendre ses images à taux préférentiel : 70% (au lieu de 40% pour les abonnés)
- Possibilité de créer des cartes privée partageable sur votre site internet
- Etre missioner pour réaliser un reportage auprès des OT partenaires


